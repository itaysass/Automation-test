    private void btn_CheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CheckActionPerformed
        String getCelc = TextFiled_Celsius.getText();

        if (!getCelc.isEmpty()) {
            double celc = Double.parseDouble(TextFiled_Celsius.getText());
            double fer = (celc * 1.8000) + 32;

            //  fer = Double.parseDouble(new DecimalFormat("#.##").format(fer));
            String ans = df2.format(fer) + "Â°F";
            CheckCTF.setText(ans);
        }
    }//GEN-LAST:event_btn_CheckActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String getCelc = TextFiled_Celsius.getText();
        if (!getCelc.isEmpty()) {
            auto.startSelenium();
            String result = auto.CelsiusToFahrenheit(Double.parseDouble(TextFiled_Celsius.getText()));
            TestFiled_Fahrenheit.setText(result);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btn_Check_OTGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Check_OTGActionPerformed

        String meeters = TextFiled_Meters1.getText();
        if (!meeters.isEmpty()) {
            double num = Double.parseDouble(meeters);
            double result = (num * 3.2808);

            String ans = df2.format(result) + "ft";
            CheckMTF1.setText(ans);
        }
    }//GEN-LAST:event_btn_Check_OTGActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String meeters = TextFiled_Meters1.getText();
        if (!meeters.isEmpty()) {
            double num = Double.parseDouble(meeters);
            auto.startSelenium();
            String result = auto.MeterstoFeet(num);
            TestFiled_Feet.setText(result);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btn_Check_mTf1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Check_mTf1ActionPerformed
        String onuces = TextFiled_ounces.getText();
        if (!onuces.isEmpty()) {
            double num = Double.parseDouble(onuces);
            double result = (num / 0.035274);
            String ans = df2.format(result) + "g";
            // String ans = Double.toString(result) + "g";
            CheckOTG.setText(ans);
        }
    }//GEN-LAST:event_btn_Check_mTf1ActionPerformed

